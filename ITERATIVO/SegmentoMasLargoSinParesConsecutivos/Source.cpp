int resolver(const vector<int>& v, int k, int n) {

    int tam = 0;
    int pares = 0;
    int iniNew = 0;

    for (int i = 0; i < n; i++)
    {
        if (v[i] % 2 == 0) {
            pares++;
            if (pares > k) {
                iniNew = i - (k - 1);
                pares = k;
            }
        }
        else {
            pares = 0;
        }
        tam = max(tam, (i - iniNew + 1));
    }
    return tam;



}

void resuelveCaso() {

    int n, k, a;

    cin >> n >> k;

    vector<int> v;

    for (int i = 0; i < n; i++) {
        cin >> a;
        v.push_back(a);
    }

    cout << resolver(v, k, n) << endl;
    
}

